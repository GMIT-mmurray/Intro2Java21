package ie.gmit.Lesson03.Exercise08;

public class Looping {
  public static void main(String[] args) {
    System.out.println("Increasing order");
    for (int i = 0; i <= 9; i++)
      System.out.println(i);
    System.out.println("Decreasing order");
    for (int i = 9; i >= 0; i--)
      System.out.println(i);
  }
}